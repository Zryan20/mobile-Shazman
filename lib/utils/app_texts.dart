  // Get greeting based on time of day
  static String getGreeting(String name) {
    final hour = DateTime.now().hour;
    if (hour < 12) {
      return greetingMorning(name);
    } else if (hour < 17) {
      return greetingAfternoon(name);
    } else {
      return greetingEvening(name);
    }
